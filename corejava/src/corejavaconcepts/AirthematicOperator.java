package corejavaconcepts;

public class AirthematicOperator {
	 public static void main(String[] args) {
		    int i = 5 + 2;
		    int j = i - 1;
		    int k = j * 2;
		    double l = k / 6;

		    System.out.println("i = " + i);
		    System.out.println("j = " + j);
		    System.out.println("k = " + k);
		    System.out.println("l = " + l);
		  }
		}
